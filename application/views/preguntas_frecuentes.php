	<p>¿Si ya terminé un curso y deseo ingresar a otro, necesito llenar
		todo el formulario nuevamente?</p>
	
	
	<blockquote>
		<strong>No, si has terminado un curso o deseas ingresar a otro de tu
			interés, sólo debes dar clic en el botón “Inscribirme a este curso” y
			aceptar la confirmación para comenzar a trabajar en él.</strong>
	</blockquote>
	

	<p>¿Me puedo registrar a más de un curso?</p>
	
	
	<blockquote>
		<strong>Sí, pero debes considerar que pasados 30 días naturales sin
			actividad en cada curso se te suspenderá el acceso, por ello te
			sugerimos tomar un curso a la vez.</strong>
	</blockquote>


	<p>¿Qué tengo que hacer para obtener la constancia de término del
		curso?</p>
	
	
	<blockquote>
		<strong>Es necesario aprobar con un 80% de reactivos correctos, en
			cada una de las evaluaciones modulares así como en la final.</strong>
	</blockquote>


	<p>¿Para revisar el contenido de cada módulo es necesario que realice
		las evaluaciones?</p>
	
	
	<blockquote>
		<strong>El contenido de cada módulo está disponible y no es necesario
			realizar las evaluaciones modulares y la final, si tu intención es
			solo de consulta.</strong>
	</blockquote>


	<p>¿A quién le tengo que solicitar la constancia de terminación del
		curso?</p>
	
	
	<blockquote>
		<strong>La constancia de terminación del curso se encuentra disponible
			una vez aprobada la evaluación final con un mínimo de 80% de
			reactivos correctos, solo así podrás descargarla.</strong>
	</blockquote>
	
