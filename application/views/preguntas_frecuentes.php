         <div id="contenido-texto" class="faq">
         <p>¿Cuáles son los requisitos técnicos que necesito para navegar en la plataforma del Centro de Capacitación Virtual?
		<blockquote><b>Es importante contar con el navegador de tu preferencia actualizado o de preferencia Google Chrome, así como de un lector para formatos en PDF y tener actualizado JAVA para los vídeo que en algunos casos las editoriales proprocionan.</b></blockquote></p>
		
		<p>¿Si tengo problemas técnicos con quién me tengo que dirigir?
		<blockquote><b>Para la atención de problemas técnicos, puedes enviar un correo a: <a href="mailto:centro.capacitacion@conricyt.mx">centro.capacitacion@conricyt.mx</a> o a los teléfonos: + 55 53 22 77 00 exts. 4020 - 4026</b></blockquote></p>
		
		<p>¿Puedo entrar al curso que desee o necesito que mi institución pertenezca CONRICYT?
		<blockquote><b>Los cursos del Centro de Capacitación Virtual del CONRICYT se encuentran disponibles a todo el público interesado en conocer el funcionamiento de las plataformas que el Consorcio suscribe, sin importar si tú institución pertenece o no al CONRICYT.</b></blockquote></p>
		
		<p>¿Puedo entrar a los cursos si mi institución ya no pertenece o nunca ha pertenecido al CONRICYT?
		<blockquote><b>Los cursos son abiertos a todo público aun cuando tú institución no pertenezca o haya pertenecido al CONRICYT</b></blockquote></p>
		
		<p>¿Los cursos cuentan con algún tutor que me pueda orientar o a quién le puedo expresar mis dudas?
		<blockquote><b>Si bien, los cursos del Centro de Capacitación Virtual son autogestivos puedes contactarnos para atender tus dudas por correo electrónico a: <a href="mailto:centro.capacitacion@conricyt.mx">centro.capacitacion@conricyt.mx</a> y a la brevedad daremos solución a tus inquietudes</b></blockquote></p>
		 
		<p>¿Si me desmatriculan qué puedo hacer para recuperar el curso?
		<blockquote><b>Si eres desmatriculado de algún curso, debes realizar nuevamente el registro para ingresar nuevamente al curso y comenzarlo dede el inicio</b></blockquote></p>
		
		<p>¿Es obligatorio que haga la evaluación por módulo y la evaluación final?
		<blockquote><b>Es impresindible que se realicen las evaluaciones por módulo con un 80% mínimo de reactivos correctos, de lo contrario no podras ingresar al módulo siguiente y consecuentemente a la evaluación final.
		<br><br>
		La evaluación final es obligatoria siempre y cuando desees obtener la constancia que emite la editorial. Si tu deseas realizar la evaluación final pero no requeires la constancia puedes aplicar la evaluación sin ningún problema.</b></blockquote></p>
		
		<p>¿Si ya terminé un curso y deseo ingresar a otro, necesito llenar todo el formulario nuevamente?
		<blockquote><b>No, si has terminado un curso y deseas ingresar a otro que te interese, solo debes selecionarlo y comenzar a trabajar en él.</b></blockquote></p>
		
		<p>¿Qué pasa si no termino un curso pero quiero entrar a otro?
		<blockquote><b>No pasa absolutamente nada, pero debes tomar en cuenta que si te matriculas en mas de dos cursos y en 30 días no realizas ningúna actividad se te desmatriculará de los cursos, por ello sugerimos ingresar a uno y terminalo para ingresar a otro.</b></blockquote></p>
		
		<p>¿A quién le tengo que solicitar la constancia de terminación del curso?
		<blockquote><b>La constancia de terminación del curso se encuentra disponible una vez que haya aprobado la evaluación final con un mínimo de 80% de reactivos correctos, solo así podras descargar la constancia.
		<br><br>
		Sí cubriste el mínimo de reactivos correctos, se habilita la opción de Descargar Constancia de Terminación del Curso.</b></blockquote></p>
		
		<p>¿Tiene algún valor curricular la constancia de terminación del curso?
		<blockquote><b>El valor curricular de la constancia la determina la propia editorial, en algunos casos el documento cuenta con la leyenda:
		<i>“Este documento no tiene níngun valor curricular”</i></b></blockquote></p>
         
