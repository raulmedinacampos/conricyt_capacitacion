					<p>¿Es posible realizar el registro a un curso, aun cuando la
						institución no es beneficiada por el CONRICYT?</p>


					<blockquote>
						<strong>Los cursos del Centro de Capacitación Virtual, están
							disponibles a todo usuario interesado en aprender a utilizar las
							plataformas de las editoriales, para buscar y recuperar
							información científica y tecnológica que cubra las necesidades de
							información, por lo que el registro a un curso no es exclusivo
							para las instituciones que forman parte del Consorcio.</strong>
					</blockquote>

					<p>¿El registro debe ser a un solo curso o es posible registrar
						varios cursos del Centro de Capacitación?</p>
					<blockquote>
						<strong>Existe la posibilidad de elegir uno o más cursos que se
							encuentren disponibles, desde la primera vez que se realiza el
							registro. Es importante considerar que cada curso requiere en
							promedio 20 horas (no continuas) para su conclusión.</strong>
					</blockquote>


					<p>¿Qué es lo que se debe hacer para ingresar al curso si se
						suspende el acceso?</p>


					<blockquote>
						<strong>Por lo general se suspendería el acceso a un curso, si el
							usuario presenta en la Plataforma inactividad durante 30
							(treinta) días naturales. Para que se reactivar el curso de debe
							enviar por correo electrónico la solicitud, escribiendo en el
							Asunto del correo “Solicitud de activación” y en el cuerpo del
							mismo el nombre completo, el correo electrónico con el que se
							realizó el registro y comprometerse a finalizar el curso en los
							30 días naturales posteriores a la reactivación.</strong>
					</blockquote>

